const FILTERS_PATH = join(CLAUDEVIEW_DIR, 'filters.json');
const STATS_PATH = join(CLAUDEVIEW_DIR, 'output-stats.json');

export interface FilterRule {
  readonly pattern: string;
  readonly maxLines: number;
  readonly tailLines?: number;
  readonly description: string;
  readonly enabled: boolean;
  readonly autoGenerated: boolean;
  readonly hitCount: number;
  readonly totalTokensSaved: number;
}

export interface OutputStat {
  readonly command: string;
  readonly normalizedCommand: string;
  readonly outputLines: number;
  readonly outputChars: number;
  readonly estimatedTokens: number;
  readonly timestamp: string;
  readonly count: number;
}

export interface FilterConfig {
  readonly rules: readonly FilterRule[];
  readonly globalMaxLines: number;
  readonly enabled: boolean;
}

